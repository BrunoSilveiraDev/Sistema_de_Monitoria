    private void btInscreverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btInscreverMouseClicked

        if (tbHoraDisp.getSelectedRow() > -1) {
            MonitoriaNBD mf = monitorias.get(tbHoraDisp.getSelectedRow());
            Lotacao lot = new Lotacao();
            lot.setMoaid(mf.getId());
            lot.setUsumatricula(usuario.getMatricula());
            if (!mf.getMonitor().getMatricula().equals(usuario.getMatricula())) {
                try {
                    Alert.aviso(controller.addLotacao(lot));
                    updateView();
                } catch (SQLIntegrityConstraintViolationException de) {
                    Alert.aviso("Este horario já esta reservado.");
                } catch (DAOException | SQLException ex) {
                    Logger.getLogger(ViewAlunoSelMonitoria.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                Alert.aviso("Voce nao pode se cadastrar na sua propria monitoria.");
            }
        } else {
            Alert.aviso("Selecione uma linha para se inscrever.");
        }
    }//GEN-LAST:event_btInscreverMouseClicked

    private void btRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btRemoverMouseClicked
        if (tbHorAluno.getSelectedRow() > -1) {
            MonitoriaNBD lot = lotacoes.get(tbHorAluno.getSelectedRow());
            try {
                controller.delLotacao(lot, usuario.getMatricula());
                updateView();
            } catch (DAOException | SQLException ex) {
                Logger.getLogger(ViewAlunoSelMonitoria.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            Alert.aviso("Selecione uma linha para remover.");
        }
    }//GEN-LAST:event_btRemoverMouseClicked

