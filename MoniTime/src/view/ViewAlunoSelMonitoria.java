    public void setDataSet(List<MonitoriaNBD> monitorias, List<MonitoriaNBD> lots) {
        tableModel1 = new ViewAlunoSelMonitoriaTM(monitorias);
        tableModel2 = new ViewAlunoSelMonitoriaTM2(lots);
        tbHoraDisp.setModel(tableModel1);//vazio
        tbHorAluno.setModel(tableModel2);
        this.monitorias = monitorias;//fill monitorias
        this.lotacoes = lots;//fill monitorias
    }

    public void updateView() throws SQLException, DAOException {
        this.monitorias = controller.getAllMoniFuncionais();
        tableModel1.refresh(this.monitorias);
        this.lotacoes = controller.getAllLotacaoMonis(usuario.getMatricula());
        tableModel2.refresh(this.lotacoes);
    }

