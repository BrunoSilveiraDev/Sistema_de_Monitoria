    private void btSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSelecionarMouseClicked
        try {
            if (tbHorDisponivel.getSelectedRow() > -1) {
                Sala sl = salas.get(tbHorDisponivel.getSelectedRow());
                if (sl.getOcupacao().equals("livre")) {
                    ctrlMntorSelSala.atualizaOcupacao(sl, monitor.getNome());
                    MonitoriaNBD moniF = new MonitoriaNBD(monitor, sl);
                    ctrlAlunoSelMonitoria.addMonitoria(moniF);
                    updateView();
                } else {
                    Alert.aviso("Selecione uma sala livre.");
                }
            } else {
                Alert.aviso("Selecione um horario na tabela.");
            }
        } catch (DAOException | SQLException ex) {
            Logger.getLogger(ViewMonitorSelecaoSala.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btSelecionarMouseClicked

