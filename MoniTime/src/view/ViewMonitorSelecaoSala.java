    public void setDataSet(List<Sala> salas) {
        tableModel1 = new ViewMonitorSelDispTM(salas);
        tbHorDisponivel.setModel(tableModel1);//vazio
        tableModel2 = new ViewMonitorSelPossuiTM(salas);
        tbHorMonitor.setModel(tableModel2);//vazio
        this.salas = salas;//fill monitorias
    }

    public void updateView() throws SQLException, DAOException {
        this.salas = ctrlMntorSelSala.getAllData();
        tableModel1.refresh(this.salas);
        tableModel2.refresh(this.salas, monitor.getNome());
        if (tableModel2.getRowCount() == 1) {
            lbTempoGasto.setText("Tempo dedicado a monitoria: " + tableModel2.getRowCount() + " hora.");
        } else {
            lbTempoGasto.setText("Tempo dedicado a monitoria: " + tableModel2.getRowCount() + " horas.");
        }
        if (tableModel2.getRowCount() < 6) {
            lbTempoGasto.setForeground(Color.red);
            Alert.aviso("Voce esta abaixo do tempo minimo de 6 horas por semana.");
        } else {
            lbTempoGasto.setForeground(Color.green);
        }
    }

