    private void btRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btRemoverMouseClicked
        try {
            if (tbHorMonitor.getSelectedRow() > -1) {
                Sala sl = tableModel2.getSalas().get(tbHorMonitor.getSelectedRow());
                ctrlMntorSelSala.atualizaOcupacao(sl, "livre");
                MonitoriaNBD moniF = new MonitoriaNBD(monitor, sl);
                ctrlAlunoSelMonitoria.delMonitoriaProc(moniF);
                updateView();
            } else {
                Alert.aviso("Selecione um horario na tabela.");
            }
        } catch (DAOException | SQLException ex) {
            Logger.getLogger(ViewMonitorSelecaoSala.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverMouseClicked

    private void btVerLotacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btVerLotacaoMouseClicked
        if (tbHorMonitor.getSelectedRow() > -1) {
            Sala sl = tableModel2.getSalas().get(tbHorMonitor.getSelectedRow());
            MonitoriaNBD moniF = new MonitoriaNBD(monitor, sl);
            try {
                ;
                ViewVerLotacao verLot = new ViewVerLotacao(ctrlAlunoSelMonitoria.lotMonitoria(moniF));
                verLot.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                verLot.setLocationRelativeTo(null);
                verLot.setVisible(true);
            } catch (DAOException | SQLException ex) {
                Logger.getLogger(ViewMonitorSelecaoSala.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btVerLotacaoMouseClicked

